+ (UIImage *)scaleImage:(UIImage *)image toScale:(float)scaleSize
{
    UIGraphicsBeginImageContext(CGSizeMake(image.size.width * scaleSize, image.size.height * scaleSize));
    [image drawInRect:CGRectMake(0, 0, image.size.width * scaleSize, image.size.height * scaleSize)];
    UIImage *scaledImage = UIGraphicsGetImageFromCurrentImageContext();
    UIGraphicsEndImageContext();
    
    return scaledImage;
}

+ (UIImage *)getGradientImageWithRectSize:(CGSize)size
                            andFirstColor:(CGColorRef)firstColor
                           andSecondColor:(CGColorRef)secondColor
                            andThirdColor:(CGColorRef)thirdColor{
    return [UIImage getGradientImageWithRectSize:size
                                    andLocations:(CGFloat []){0.0, 0.2, 1.0}
                                   andFirstColor:firstColor
                                  andSecondColor:secondColor
                                   andThirdColor:thirdColor];
}

+ (UIImage *)getGradientImageWithRectSize:(CGSize)size
                             andLocations:(CGFloat [])locations
                            andFirstColor:(CGColorRef)firstColor
                           andSecondColor:(CGColorRef)secondColor
                            andThirdColor:(CGColorRef)thirdColor{
    //获取当前上下文
    UIGraphicsBeginImageContext(size);
    CGContextRef context = UIGraphicsGetCurrentContext();
    
    //绘制矩形
    CGRect rect = CGRectMake(0, 0, size.width, size.height);
    CGContextAddRect(context, rect);
    
    //获取颜色空间
    CGColorSpaceRef colorSpace = CGColorSpaceCreateDeviceRGB();
    //设置渐变色
    NSArray *colors = @[(__bridge id) firstColor, (__bridge id) secondColor, (__bridge id) thirdColor];
    //创建渐变色
    CGGradientRef gradient = CGGradientCreateWithColors(colorSpace, (__bridge CFArrayRef) colors, locations);
    //设置渐变色起点
    CGPoint startPoint = (CGPoint){size.width * 0, size.height * 0};
    //设置渐变色终点
    CGPoint endPoint = (CGPoint){size.width * 1.0, size.height * 0.0};
    //绘制渐变色
    CGContextDrawLinearGradient(context, gradient, startPoint, endPoint, 0);
    //释放颜色空间
    CGColorSpaceRelease(colorSpace);
    //释放渐变色
    CGGradientRelease(gradient);
    
    //从上下文中获取图像
    UIImage *image = UIGraphicsGetImageFromCurrentImageContext();
    UIGraphicsEndImageContext();
    
    return image;
}

